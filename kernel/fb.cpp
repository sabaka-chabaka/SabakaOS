static uint8_t* s_fb       = nullptr;
static uint32_t s_pitch    = 0;
static uint32_t s_width    = 0;
static uint32_t s_height   = 0;
static uint8_t  s_bpp      = 0;
static bool     s_ready    = false;

static inline void bga_write(uint16_t index, uint16_t value) {
    __asm__ volatile("outw %0,%1"::"a"(index),"Nd"((uint16_t)0x01CE));
    __asm__ volatile("outw %0,%1"::"a"(value),"Nd"((uint16_t)0x01CF));
}
static inline uint16_t bga_read(uint16_t index) {
    __asm__ volatile("outw %0,%1"::"a"(index),"Nd"((uint16_t)0x01CE));
    uint16_t v; __asm__ volatile("inw %1,%0":"=a"(v):"Nd"((uint16_t)0x01CF));
    return v;
}
static bool bga_detect() {
    uint16_t id = bga_read(0);
    return (id >= 0xB0C0 && id <= 0xB0CF);
}
static bool bga_set_mode(uint16_t w, uint16_t h, uint16_t bpp) {
    bga_write(4, 0x00);
    bga_write(1, w);
    bga_write(2, h);
    bga_write(3, bpp);
    bga_write(6, w);
    bga_write(7, h);
    bga_write(8, 0);
    bga_write(9, 0);
    bga_write(4, 0x01 | 0x40); // ENABLED | LFB_ENABLED
    return (bga_read(1) == w && bga_read(2) == h);
}

static uint32_t pci_find_bga_lfb() {
    for (uint8_t bus = 0; bus < 8; bus++) {
        for (uint8_t dev = 0; dev < 32; dev++) {
            uint32_t addr = 0x80000000u | ((uint32_t)bus<<16) | ((uint32_t)dev<<11);
            __asm__ volatile("outl %0,%1"::"a"(addr),"Nd"((uint16_t)0xCF8));
            uint32_t vd; __asm__ volatile("inl %1,%0":"=a"(vd):"Nd"((uint16_t)0xCFC));
            if (vd == 0x11111234u) {
                __asm__ volatile("outl %0,%1"::"a"(addr|0x10),"Nd"((uint16_t)0xCF8));
                uint32_t bar0; __asm__ volatile("inl %1,%0":"=a"(bar0):"Nd"((uint16_t)0xCFC));
                return bar0 & 0xFFFFFFF0u;
            }
        }
    }
    return 0;
}

bool fb_init(const MultibootInfo* mbi) {
    if (bga_detect()) {
        if (bga_set_mode(1024, 768, 32)) {
            uint32_t lfb = pci_find_bga_lfb();
            if (lfb == 0) lfb = 0xE0000000u;
            s_fb     = (uint8_t*)lfb;
            s_width  = 1024;
            s_height = 768;
            s_bpp    = 32;
            s_pitch  = 1024 * 4;
            s_ready  = true;
            return true;
        }
    }

    if (!mbi) return false;
    if (!(mbi->flags & (1u << 12))) return false;
    if (mbi->framebuffer_type == 0) return false;
    if (mbi->framebuffer_bpp != 32 &&
        mbi->framebuffer_bpp != 24 &&
        mbi->framebuffer_bpp != 16) return false;
    if (mbi->framebuffer_width < 320 || mbi->framebuffer_height < 200) return false;
    s_fb     = (uint8_t*)(uintptr_t)mbi->framebuffer_addr;
    s_pitch  = mbi->framebuffer_pitch;
    s_width  = mbi->framebuffer_width;
    s_height = mbi->framebuffer_height;
    s_bpp    = mbi->framebuffer_bpp;
    s_ready  = true;
    return true;
}

bool     fb_available() { return s_ready; }
uint32_t fb_phys_addr()  { return (uint32_t)(uintptr_t)s_fb; }
uint32_t fb_size_bytes() { return s_pitch * s_height; }
uint32_t fb_width()     { return s_width; }
uint32_t fb_height()    { return s_height; }
uint8_t  fb_bpp()       { return s_bpp; }

static inline void put(int x, int y, uint32_t color) {
    uint8_t* p = s_fb + (uint32_t)y * s_pitch + (uint32_t)x * (s_bpp / 8);
    uint8_t r = (uint8_t)(color >> 16);
    uint8_t g = (uint8_t)(color >>  8);
    uint8_t b = (uint8_t)(color);
    if (s_bpp == 32) {
        p[0] = b; p[1] = g; p[2] = r; p[3] = 0;
    } else if (s_bpp == 24) {
        p[0] = b; p[1] = g; p[2] = r;
    } else if (s_bpp == 16) {
        uint16_t px = ((uint16_t)(r >> 3) << 11)
                    | ((uint16_t)(g >> 2) <<  5)
                    |  (uint16_t)(b >> 3);
        p[0] = (uint8_t)(px & 0xFF);
        p[1] = (uint8_t)(px >> 8);
    }
}

void fb_put_pixel(int x, int y, uint32_t color) {
    if (!s_ready) return;
    if ((uint32_t)x >= s_width || (uint32_t)y >= s_height) return;
    put(x, y, color);
}

void fb_fill_rect(int x, int y, int w, int h, uint32_t color) {
    if (!s_ready) return;
    int x1 = x < 0 ? 0 : x;
    int y1 = y < 0 ? 0 : y;
    int x2 = x + w; if ((uint32_t)x2 > s_width)  x2 = (int)s_width;
    int y2 = y + h; if ((uint32_t)y2 > s_height) y2 = (int)s_height;
    for (int row = y1; row < y2; row++)
        for (int col = x1; col < x2; col++)
            put(col, row, color);
}

void fb_draw_hline(int x, int y, int len, uint32_t color) {
    fb_fill_rect(x, y, len, 1, color);
}
void fb_draw_vline(int x, int y, int len, uint32_t color) {
    fb_fill_rect(x, y, 1, len, color);
}
void fb_draw_rect(int x, int y, int w, int h, uint32_t color) {
    fb_draw_hline(x,     y,       w, color);
    fb_draw_hline(x,     y+h-1,   w, color);
    fb_draw_vline(x,     y,       h, color);
    fb_draw_vline(x+w-1, y,       h, color);
}

void fb_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (!s_ready) return;
    int idx = (unsigned char)c - 0x20;
    if (idx < 0 || idx >= 95) idx = 0;
    const uint8_t* g = FONT[idx];
    for (int row = 0; row < FB_FONT_H; row++) {
        uint8_t bits = g[row];
        for (int col = 0; col < FB_FONT_W; col++) {
            uint32_t color = (bits & (0x80u >> col)) ? fg : bg;
            int px = x + col, py = y + row;
            if ((uint32_t)px < s_width && (uint32_t)py < s_height)
                put(px, py, color);
        }
    }
}

void fb_draw_str(int x, int y, const char* s, uint32_t fg, uint32_t bg) {
    if (!s_ready || !s) return;
    int cx = x;
    while (*s) {
        fb_draw_char(cx, y, *s++, fg, bg);
        cx += FB_FONT_W;
    }
}

void fb_draw_str_transparent(int x, int y, const char* s, uint32_t fg) {
    if (!s_ready || !s) return;
    int cx = x;
    while (*s) {
        int idx = (unsigned char)*s - 0x20;
        if (idx < 0 || idx >= 95) idx = 0;
        const uint8_t* g = FONT[idx];
        for (int row = 0; row < FB_FONT_H; row++) {
            uint8_t bits = g[row];
            for (int col = 0; col < FB_FONT_W; col++) {
                if (bits & (0x80u >> col)) {
                    int px = cx + col, py = y + row;
                    if ((uint32_t)px < s_width && (uint32_t)py < s_height)
                        put(px, py, fg);
                }
            }
        }
        cx += FB_FONT_W;
        s++;
    }
}

void fb_scroll_region(int rx, int ry, int rw, int rh,
                      int lines, uint32_t fill_color)
{
    if (!s_ready || lines <= 0) return;
    int px_lines = lines * FB_FONT_H;
    if (px_lines >= rh) {
        fb_fill_rect(rx, ry, rw, rh, fill_color);
        return;
    }
    uint8_t bpb = s_bpp / 8;
    int src_y = ry + px_lines;
    int dst_y = ry;
    int copy_rows = rh - px_lines;

    for (int row = 0; row < copy_rows; row++) {
        uint8_t* src = s_fb + (uint32_t)(src_y + row) * s_pitch + (uint32_t)rx * bpb;
        uint8_t* dst = s_fb + (uint32_t)(dst_y + row) * s_pitch + (uint32_t)rx * bpb;
        uint32_t bytes = (uint32_t)rw * bpb;
        for (uint32_t i = 0; i < bytes; i++) dst[i] = src[i];
    }
    fb_fill_rect(rx, ry + copy_rows, rw, px_lines, fill_color);
}