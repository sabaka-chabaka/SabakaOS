static const int FONT_W = 8;
static const int FONT_H = 16;

static uint32_t* s_fb     = nullptr;
static uint32_t  s_pitch4 = 0;
static uint32_t  s_width  = 0;
static uint32_t  s_height = 0;
static uint8_t   s_bpp    = 0;
static bool      s_ready  = false;

static inline void write_pixel(uint32_t* base, uint32_t pitch4,
                                int x, int y, uint32_t rgb)
{
    uint8_t* row = (uint8_t*)base + (uint32_t)y * pitch4 * 4;
    if (s_bpp == 32) {
        ((uint32_t*)row)[x] = rgb;
    } else if (s_bpp == 24) {
        uint8_t* p = row + x * 3;
        p[0] = (uint8_t)(rgb);
        p[1] = (uint8_t)(rgb >> 8);
        p[2] = (uint8_t)(rgb >> 16);
    }
}

bool fb_init(const MultibootInfo* mbi) {
    if (!mbi || !(mbi->flags & (1 << 12))) return false;
    if (mbi->framebuffer_type == 0 /* EGA text */) return false;
    if (mbi->framebuffer_bpp != 32 && mbi->framebuffer_bpp != 24) return false;

    s_fb     = (uint32_t*)(uintptr_t)mbi->framebuffer_addr;
    s_width  = mbi->framebuffer_width;
    s_height = mbi->framebuffer_height;
    s_bpp    = mbi->framebuffer_bpp;
    s_pitch4 = mbi->framebuffer_pitch / 4;
    s_ready  = true;
    return true;
}

bool     fb_available() { return s_ready; }
uint32_t fb_width()     { return s_width; }
uint32_t fb_height()    { return s_height; }

void fb_put_pixel(int x, int y, uint32_t rgb) {
    if (!s_ready) return;
    if ((uint32_t)x >= s_width || (uint32_t)y >= s_height) return;
    write_pixel(s_fb, s_pitch4, x, y, rgb);
}

void fb_fill_rect(int x, int y, int w, int h, uint32_t rgb) {
    if (!s_ready) return;
    for (int row = y; row < y + h; row++) {
        if ((uint32_t)row >= s_height) break;
        if (row < 0) continue;
        for (int col = x; col < x + w; col++) {
            if ((uint32_t)col >= s_width) break;
            if (col < 0) continue;
            write_pixel(s_fb, s_pitch4, col, row, rgb);
        }
    }
}

void fb_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (!s_ready) return;
    int idx = (unsigned char)c - 0x20;
    if (idx < 0 || idx >= 95) idx = 0;

    const uint8_t* glyph = FONT8X16[idx];
    for (int row = 0; row < FONT_H; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_W; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_put_pixel(x + col, y + row, color);
        }
    }
}

void fb_draw_str(int x, int y, const char* s, uint32_t fg, uint32_t bg) {
    if (!s_ready || !s) return;
    int cx = x;
    while (*s) {
        fb_draw_char(cx, y, *s, fg, bg);
        cx += FONT_W;
        s++;
    }
}

void fb_scroll(int lines) {
    if (!s_ready || lines <= 0) return;
    int px_lines = lines * FONT_H;
    if ((uint32_t)px_lines >= s_height) {
        fb_fill_rect(0, 0, (int)s_width, (int)s_height, 0x000000);
        return;
    }
    uint8_t* base = (uint8_t*)s_fb;
    uint32_t pitch_bytes = s_pitch4 * 4;
    uint32_t copy_bytes  = pitch_bytes * (s_height - (uint32_t)px_lines);
    uint8_t* src  = base + pitch_bytes * (uint32_t)px_lines;
    uint8_t* dst  = base;
    for (uint32_t i = 0; i < copy_bytes; i++) dst[i] = src[i];
    uint8_t* clear_start = base + pitch_bytes * (s_height - (uint32_t)px_lines);
    uint32_t clear_bytes = pitch_bytes * (uint32_t)px_lines;
    for (uint32_t i = 0; i < clear_bytes; i++) clear_start[i] = 0;
}