void vesa_init(const VesaInfo *info) {
    g_vesa = *info;
    g_available = (info->addr != 0 && info->bpp == 32 && info->type == 1);
}

bool vesa_available() { return g_available; }
int  vesa_width()     { return (int)g_vesa.width; }
int  vesa_height()    { return (int)g_vesa.height; }

void vesa_put_pixel(int x, int y, uint32_t rgb) {
    if (!g_available) return;
    if ((uint32_t)x >= g_vesa.width || (uint32_t)y >= g_vesa.height) return;
    uint32_t* fb = (uint32_t*)(g_vesa.addr + (uint32_t)y * g_vesa.pitch);
    fb[x] = rgb;
}

void vesa_fill_rect(int x, int y, int w, int h, uint32_t rgb) {
    if (!g_available) return;
    for (int row = y; row < y + h; row++) {
        uint32_t* line = (uint32_t*)(g_vesa.addr + (uint32_t)row * g_vesa.pitch);
        for (int col = x; col < x + w; col++)
            line[col] = rgb;
    }
}

void vesa_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (!g_available) return;
    uint8_t idx = (uint8_t)c;
    if (idx >= 128) idx = 0;
    const uint8_t* glyph = FONT_8x16[idx];
    for (int row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        uint32_t* line = (uint32_t*)(g_vesa.addr + (uint32_t)(y + row) * g_vesa.pitch);
        for (int col = 0; col < 8; col++) {
            line[x + col] = (bits & (0x80 >> col)) ? fg : bg;
        }
    }
}